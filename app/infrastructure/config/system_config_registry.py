def get_db_system_config_definition(key: str) -> SystemConfigDefinition | None:
    return DB_SYSTEM_CONFIGS_BY_KEY.get(key)


def is_db_system_config_key(key: str | None) -> bool:
    return bool(key) and key in DB_SYSTEM_CONFIG_KEYS


def is_env_only_system_config_key(key: str | None) -> bool:
    return bool(key) and key in ENV_ONLY_SYSTEM_CONFIG_KEYS


def list_db_system_config_definitions() -> list[SystemConfigDefinition]:
    return list(DB_SYSTEM_CONFIG_DEFINITIONS)
