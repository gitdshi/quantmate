from __future__ import annotations

from dataclasses import asdict, dataclass
from typing import Literal

SystemConfigValueType = Literal["string", "int", "float", "bool", "csv"]


@dataclass(frozen=True)
class SystemConfigDefinition:
    key: str
    category: str
    label: str
    description: str
    value_type: SystemConfigValueType
    default_value: str
    legacy_env_keys: tuple[str, ...] = ()

    def to_dict(self) -> dict[str, object]:
        return asdict(self)


def _cfg(
    key: str,
    *,
    category: str,
    label: str,
    description: str,
    value_type: SystemConfigValueType,
    default_value: str,
    legacy_env_keys: str | tuple[str, ...] = (),
) -> SystemConfigDefinition:
    if isinstance(legacy_env_keys, str):
        normalized_env_keys = (legacy_env_keys,) if legacy_env_keys else ()
    else:
        normalized_env_keys = tuple(item for item in legacy_env_keys if item)
    return SystemConfigDefinition(
        key=key,
        category=category,
        label=label,
        description=description,
        value_type=value_type,
        default_value=default_value,
        legacy_env_keys=normalized_env_keys,
    )


